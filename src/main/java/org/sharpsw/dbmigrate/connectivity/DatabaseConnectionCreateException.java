package org.sharpsw.dbmigrate.connectivity;

public class DatabaseConnectionCreateException extends Exception {
	private static final long serialVersionUID = 1625345203725775476L;

	public DatabaseConnectionCreateException() {
	}

	public DatabaseConnectionCreateException(String arg0) {
		super(arg0);
	}

	public DatabaseConnectionCreateException(Throwable arg0) {
		super(arg0);
	}

	public DatabaseConnectionCreateException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
