package org.sharpsw.dbmigrate.utility;

public class DatabaseSchemaExportException extends Exception {

	private static final long serialVersionUID = 1L;

	public DatabaseSchemaExportException() {
	}

	public DatabaseSchemaExportException(String arg0) {
		super(arg0);
	}

	public DatabaseSchemaExportException(Throwable arg0) {
		super(arg0);
	}

	public DatabaseSchemaExportException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
