package org.sharpsw.dbmt.connectivity;

public class DBConnectionCreateException extends Exception {
	private static final long serialVersionUID = 1625345203725775476L;

	public DBConnectionCreateException() {
	}

	public DBConnectionCreateException(String arg0) {
		super(arg0);
	}

	public DBConnectionCreateException(Throwable arg0) {
		super(arg0);
	}

	public DBConnectionCreateException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
