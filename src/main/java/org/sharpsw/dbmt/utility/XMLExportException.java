package org.sharpsw.dbmt.utility;

public class XMLExportException extends Exception {

	private static final long serialVersionUID = 1L;

	public XMLExportException() {
	}

	public XMLExportException(String arg0) {
		super(arg0);
	}

	public XMLExportException(Throwable arg0) {
		super(arg0);
	}

	public XMLExportException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
