package org.codecraftlabs.neptune.connectivity;

public class DatabaseConnectionFactoryException extends Exception {
	private static final long serialVersionUID = 1625345203725775476L;

	public DatabaseConnectionFactoryException() {
	}

	public DatabaseConnectionFactoryException(String arg0) {
		super(arg0);
	}

	public DatabaseConnectionFactoryException(Throwable arg0) {
		super(arg0);
	}

	public DatabaseConnectionFactoryException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
